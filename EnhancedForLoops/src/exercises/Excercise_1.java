package exercises;

public class Excercise_1 {
	// Create an array of strings and iterate through it, printing each value to console, using an enhanced for loop.
	public static void main(String[] args) {
		// Create an array of strings 
		String[] arr = {"I", "AM", "A", "STRING", "ARRAY"};
		
		for( String str : arr ) { 			// iterate through it
			System.out.print(str + " ");	// printing each value to console
		}
		
		
	}

}
